export function QRCode({ size = 200, color = "#1b1b1c" }: QRCodeProps) {
  const moduleSize = size / 21;

  return (
    <svg width={size} height={size} viewBox={`0 0 ${size} ${size}`} style={{ display: "block" }}>
      <rect width={size} height={size} fill="white" />
      {MODULES.map(([x, y], i) => (
        <rect
          key={i}
          x={x * moduleSize}
          y={y * moduleSize}
          width={moduleSize - 0.5}
          height={moduleSize - 0.5}
          fill={color}
          rx={1}
        />
      ))}
    </svg>
  );
}

